import UIKit

extension UIColor{
    class func themeBlue() -> UIColor{
        return UIColor.init(red: 63/255.0, green: 81/255.0, blue: 181/255.0, alpha: 1.0)
    }
    
    class func background() -> UIColor{
        return UIColor.init(red: 249/255, green: 249/255, blue: 249/255, alpha: 1)
    }

    class func otherServicesHeader() -> UIColor{
        return UIColor.init(red: 112/255, green: 112/255, blue: 112/255, alpha: 1)
    }

    
}
